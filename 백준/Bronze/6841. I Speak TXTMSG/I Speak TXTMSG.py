while True:
    M = input()

    if M == "TTYL":
        print("talk to you later")
        break

    if M == "CU":
        print("see you")
        continue

    if M == ":-)":
        print("I’m happy")
        continue

    if M == ":-(":
        print("I’m unhappy")
        continue

    if M == ";-)":
        print("wink")
        continue

    if M == ":-P":
        print("stick out my tongue")
        continue

    if M == "(~.~)":
        print("sleepy")
        continue

    if M == "TA":
        print("totally awesome")
        continue

    if M == "CCC":
        print("Canadian Computing Competition")
        continue

    if M == "CUZ":
        print("because")
        continue

    if M == "TY":
        print("thank-you")
        continue

    if M == "YW":
        print("you’re welcome")
        continue

    print(M)